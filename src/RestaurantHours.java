public class RestaurantHours {
    /**
     * Represents the hours the restaurant is open.
     */
    public enum Hours {
        /**
         * Shift from 8AM to 4PM.
         */
        MORNING,

        /**
         * Shift from 4PM to 12AM.
         */
        AFTERNOON,

        /**
         * Shift from 12AM to 8AM.
         */
        MIDNIGHT
    }
}
