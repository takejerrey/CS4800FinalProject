public class DriverTimeShift
{
    /**
     * Represents the shift a driver begins working at.
     */
    public enum Shift {
        /**
         * Shift from 8AM to 4PM.
         */
        MORNING,

        /**
         * Shift from 4PM to 12AM.
         */
        AFTERNOON,

        /**
         * Shift from 12AM to 8AM.
         */
        MIDNIGHT
    }
}
